export type TranslationKey = keyof typeof zh;

const MESSAGES: Record<LanguageCode, Messages> = {
  zh,
  en,
  hi,
  es,
  ar,
  fr,
  bn,
  pt,
  ru,
  ur
};

const MODE_LABEL: Record<TimerMode, TranslationKey> = {
  work: "workMode",
  shortBreak: "shortBreakMode",
  longBreak: "longBreakMode"
};

export const createTranslator = (language: LanguageCode) => {
  const dict = MESSAGES[language] ?? MESSAGES.zh;
  return (key: TranslationKey): string => dict[key] ?? zh[key];
};

export const modeLabelKey = (mode: TimerMode): TranslationKey => MODE_LABEL[mode];
